package JavaAssignments;

public class MultiplicationTableFor2And3 {
    public static void main(String[] args) {
        //2 table
        int j = 2;

        for (int i = 1; i <= 10; i++) {
            System.out.println(j+" * "+i+" = "+ j*i);
        }
        //3 table
        int k = 3;

        for (int i = 1; i <= 10; i++) {
            System.out.println(k+" * "+i+" = "+ k*i);
        }
    }



}
